public class person {
    private String name;
    private String moNum;
    private int sex;
    private String geRenJieShao;
    private String interest;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMoNum() {
        return moNum;
    }

    public void setMoNum(String moNum) {
        this.moNum = moNum;
    }

    public int getSex() {
        return sex;
    }

    public void setSex(int sex) {
        this.sex = sex;
    }

    public String getGeRenJieShao() {
        return geRenJieShao;
    }

    public void setGeRenJieShao(String geRenJieShao) {
        this.geRenJieShao = geRenJieShao;
    }

    public String getInterest() {
        return interest;
    }

    public void setInterest(String interest) {
        this.interest = interest;
    }
}
